function buildBatch(moduleName, keyList) {
  const batch = {}
  for (const locale of LOCALES) batch[locale] = {}

  let included = 0
  for (const key of keyList) {
    const t = TRANSLATIONS[key]
    if (!t) {
      console.warn(`[${moduleName}] missing translation for: ${key}`)
      continue
    }
    if (!locales.en[key]) {
      console.warn(`[${moduleName}] key not in en.json: ${key}`)
      continue
    }
    let keyChanged = false
    for (const locale of LOCALES) {
      const newVal = t[locale]
      const curVal = locales[locale][key]
      if (newVal && newVal !== curVal) {
        batch[locale][key] = newVal
        keyChanged = true
      }
    }
    if (keyChanged) included++
  }
  return { batch, included }
}

const summary = []
for (const [moduleName, keyList] of Object.entries(MODULE_KEYS)) {
  const { batch, included } = buildBatch(moduleName, keyList)
  const outPath = path.join(BATCHES_DIR, `module-${moduleName}.json`)
  await fs.writeFile(outPath, JSON.stringify(batch, null, 2) + '\n')
  const localeCounts = LOCALES.map((l) => `${l}:${Object.keys(batch[l]).length}`).join(' ')
  summary.push({ module: moduleName, keys: included, localeCounts, path: outPath })
  console.log(`module-${moduleName}.json: ${included} keys (${localeCounts})`)
}

console.log('\nSummary:', JSON.stringify(summary, null, 2))